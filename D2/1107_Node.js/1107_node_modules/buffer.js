// buffer创建   类 二进制
let buffer = Buffer.alloc(10)
console.log(buffer);
let buffer1 = Buffer.from("hello world")
console.log(buffer1.toString());
let buffer2 = Buffer.from([ 0xe4, 0xbd, 0xa0, 0xe5])
let buffer3 = Buffer.from([ 0xa5, 0xbd])
console.log(buffer2.toString());

let {StringDecoder} = require("string_decoder")
let decode = new StringDecoder()
let res1 = decode.write(buffer2)
let res2 = decode.write(buffer3)
console.log(res1+res2);