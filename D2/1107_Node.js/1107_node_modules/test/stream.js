let buffer = Buffer.alloc(65*1024)
let fs = require('fs')
fs.writeFileSync("65kb",buffer)
let rs = fs.createReadStream("65kb");
let ws = fs.createWriteStream("2.txt");
// 读rs写入ws
rs.pipe(ws)
let num = 0;
let str = "";
rs.on("data",chunk=>{
    num ++;
    str += chunk;
    console.log(num);
})
rs.on("end",()=>{
    console.log("完整数据:",str);
})